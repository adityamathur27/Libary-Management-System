    private void issueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dformat = new SimpleDateFormat("dd-MM-yyyy");
        String id = book_id.getText();
        String bookname = book_name.getText();
        String studentid = stu_id.getText();
        String issuedate = dformat.format(issue_date.getDate());
        String returndate = dformat.format(return_date.getDate());
        
        String returnbook = "NO";
        
        if(id.length() == 0 || bookname.length()==0 || studentid.length()==0 || issuedate.length()==0 || returndate.length()==0){
            JOptionPane.showMessageDialog(null,"Enter all details");
                                                
        }
        if (issuedate.compareTo(returndate) >= 0) {
                    JOptionPane.showMessageDialog(null,"Return date is greater than or equal to issue date.");
        } 
        else{
            
            try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rt = st.executeQuery("Select * from book where Book_id = '"+id+"' "); 
            
            if(rt.next()){
                ResultSet rt1 = st.executeQuery("Select * from student where student_id = '"+studentid+"'");
                if(rt1.next()){
                    st.executeUpdate("insert into issue values('"+id+"','"+bookname+"','"+studentid+"','"+issuedate+"','"+returndate+"','"+"NO"+"')");
                    JOptionPane.showMessageDialog(null,"Book Succesfully Issued");
                    setVisible(false);
                    new Issue_book().setVisible(true);
                }
                else{
                    JOptionPane.showConfirmDialog(null,"Incorrect Student Id");                                        
                }
            }
            else{
                JOptionPane.showConfirmDialog(null,"Incorrect Book Id");
            }           
            
            
        }catch(Exception E){
            
        }            
        }
        
        
        
    }//GEN-LAST:event_issueActionPerformed

