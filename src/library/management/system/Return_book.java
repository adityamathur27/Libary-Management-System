    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String bookid = book_id.getText();
        String studentid = student_id.getText();
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rt = st.executeQuery("Select * from issue where Book_id = '"+bookid+"' and Student_Id = '"+studentid+"'" );
            
            if(rt.next()){
                issue_date.setText(rt.getString(4));
                return_date.setText(rt.getString(5));
                book_id.setEditable(false);
                student_id.setEditable(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "You have not issued any book");
                setVisible(false);
                new Return_book().setVisible(true);
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Connection Error");            
        }
    }//GEN-LAST:event_searchActionPerformed

